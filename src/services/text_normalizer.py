# 拼音/英文缩写 → 中文。必须带词边界（前后不能是字母数字）再替换：
# 裸 replace 会把链接和淘口令里的随机字母改坏——u.jd.com→u.京东.com、
# ￥FQtmg95…￥→¥FQ天猫g95…，凭空注入"京东/天猫/优惠"导致关键词误命中，
# 且喂给 DS 的文本里口令已被改烂。tmall 放在 tm 前面优先整词匹配。
_ABBR_RULES: list[tuple[re.Pattern, str]] = [
    # 边界除字母数字外还要排除 . 和 /：u.jd.com 里的 jd 两边是点号，也不能替换
    (re.compile(r"(?<![A-Za-z0-9./])" + abbr + r"(?![A-Za-z0-9./])"), full)
    for abbr, full in [
        ("yh", "优惠"), ("jd", "京东"), ("tb", "淘宝"),
        ("tmall", "天猫"), ("tm", "天猫"), ("pdd", "拼多多"), ("wm", "外卖"),
    ]
]


def normalize(text: str) -> str:
    """
    将消息文本规范化：
    - 替换火星文/谐音字
    - 全角数字→半角
    - 统一货币符号
    返回规范化后的文本，原始文本不受影响。
    """
    result = text
    for old, new in CHAR_MAP.items():
        result = result.replace(old, new)
    for pat, full in _ABBR_RULES:
        result = pat.sub(full, result)
    return result


# ── CQ 码清洗（展示/补发用，供 /查 合并转发和看板补发共享）──

_CQ_IMAGE_RE = re.compile(r"\[CQ:image,[^\]]*\]")
_CQ_ANY_RE = re.compile(r"\[CQ:[^\]]*\]")
_CQ_IMAGE_TAIL_RE = re.compile(r"\[CQ:image[^\]]*$")
_CQ_ANY_TAIL_RE = re.compile(r"\[CQ:[^\]]*$")


def strip_cq(text: str, image_placeholder: str = "［图片］") -> str:
    """把消息文本里的 CQ 码清成纯展示文本。

    图片段换占位符（保留"这里原本有图"的信息）、其它 CQ 段（at/face/reply 等）
    去掉、结尾被截断的残缺 CQ 片段也清掉，最后还原 OneBot CQ 转义字符。
    直接把带 CQ 图片码的文本发出去，NapCat 会当真图下载，缓存失效/URL 过期时
    整条消息被拒（合并转发降级成长文本、补发直接失败）。"""
    if not text:
        return ""
    text = _CQ_IMAGE_RE.sub(image_placeholder, text)
    text = _CQ_ANY_RE.sub("", text)
    text = _CQ_IMAGE_TAIL_RE.sub(image_placeholder, text)
    text = _CQ_ANY_TAIL_RE.sub("", text)
    text = (text.replace("&#91;", "[").replace("&#93;", "]")
                .replace("&#44;", ",").replace("&amp;", "&"))
    return text.strip()


# ── 来源脚注 ──
# events.jsonl 里 PUSH 条目的 title 是**最终发送文本**，带展示层加的来源脚注；
# 而反馈的哈希键算的是「判定文本」（无脚注）。不先剥掉就永远对不上，
# 标「推错了」撤不掉当初那一票，同一商品的票数还会分裂。
# 两种版式都要认：
#   QQ 源 / 历史微博：正文 ───── 📌 来自xx        → 整段删掉
#   微博（2026-07-09 起）：正文 ───── 📎 微博原文：URL → 只删分隔线，📎行本就属于判定文本
_SOURCE_FOOTER_RE = re.compile(r"\n─────\n📌 [^\n]*\s*$")
_WEIBO_DIVIDER_RE = re.compile(r"\n─────\n(?=📎 微博原文：)")


def strip_footer(text: str) -> str:
    """去掉展示层加的来源脚注/分隔线，还原成 dispatch 判定时用的文本。"""
    return _WEIBO_DIVIDER_RE.sub("\n", _SOURCE_FOOTER_RE.sub("", text))
